def get_contrast_yiq(hexcolor):
    # Convert hexcolor to RGB values
    r, g, b = int(hexcolor[1:3], 16), int(hexcolor[3:5], 16), int(hexcolor[5:7], 16)

    # Calculate the YIQ (luminance) value
    yiq = ((r*299) + (g*587) + (b*114))/1000

    # Determine the best foreground color (white or black) based on the YIQ value
    return "black" if yiq < 128 else "white"

